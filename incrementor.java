public class incrementor{
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 15;

        a++;
        b++;
        c++;

        System.out.println("a after increment: " + a);
        System.out.println("b after increment: " + b);
        System.out.println("c after increment: " + c);








    }      
}  